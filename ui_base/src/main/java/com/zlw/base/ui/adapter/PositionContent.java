package com.zlw.base.ui.adapter;



public class PositionContent<T> {
    public int position = -1;
    public int resLayoutId;
    public T data;

    public PositionContent(int resLayoutId){
        this.resLayoutId = resLayoutId;
    }

    public PositionContent(int position, int resLayoutId){
        this.position = position;
        this.resLayoutId = resLayoutId;
    }

    public void setData(T data){
        this.data = data;
    }

    protected void bindItem(BaseRecyclerAdapter.BaseViewHolder holder){
    }

    protected void onRecycle(BaseRecyclerAdapter.BaseViewHolder holder){
    }

}